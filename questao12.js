/*
Questão 12 - Qual é a diferença entre map() e forEach() em relação ao retorno 
de valores de uma função de callback?

R:  O map() retorna array com novos 
    valores alterados pela função 
    callback, entretanto, o array 
    original não é alterado

    O forEach() e sua callback é usada 
    apenas para cada elemento e o array 
    original pode ser modificado
*/