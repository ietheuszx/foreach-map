/*
Questão 17 - Qual é a diferença entre map() e forEach() em termos de 
manipulação do array original?

R:  O map() não manipula diretamente o 
    array original, já que o mesmo cria 
    um novo array

    O forEach() consegue manipular 
    diretamente o array original, pois o 
    mesmo consegue alterar valores sem a 
    necessidade de um novo array
*/